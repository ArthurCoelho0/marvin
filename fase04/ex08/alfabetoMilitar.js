function alfabetoMilitar(n) {
    if(n==="a" || n==="A")
    return("alpha")
    if(n==="e" || n==="E")
    return("echo")
    if(n==="i" || n==="I")
    return("india")
    if(n==="o" || n==="O")
    return("oscar")
    if(n==="u" || n==="U")
    return("uniform")
    else
    return("false")
 }


